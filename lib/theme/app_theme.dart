import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';

/// A class that contains all theme configurations for the application.
class AppTheme {
  AppTheme._();

  // Color specifications based on Contemporary Persian Minimalism design
  static const Color primaryLight =
      Color(0xFF2E7D32); // Deep green conveying trust and success
  static const Color secondaryLight =
      Color(0xFF1565C0); // Professional blue for secondary actions
  static const Color surfaceLight =
      Color(0xFFFAFAFA); // Warm off-white reducing eye strain
  static const Color backgroundLight =
      Color(0xFFFFFFFF); // Pure white for maximum content clarity
  static const Color errorLight =
      Color(0xFFC62828); // Clear error communication
  static const Color successLight = Color(0xFF388E3C); // Confirmation feedback
  static const Color warningLight =
      Color(0xFFF57C00); // Attention-grabbing orange
  static const Color textPrimaryLight =
      Color(0xFF212121); // High contrast dark gray
  static const Color textSecondaryLight =
      Color(0xFF757575); // Subtle gray for supporting information
  static const Color dividerLight =
      Color(0xFFE0E0E0); // Minimal separation lines

  // Dark theme colors
  static const Color primaryDark =
      Color(0xFF4CAF50); // Lighter green for dark theme
  static const Color secondaryDark =
      Color(0xFF42A5F5); // Lighter blue for dark theme
  static const Color surfaceDark = Color(0xFF1E1E1E); // Dark surface
  static const Color backgroundDark = Color(0xFF121212); // Dark background
  static const Color errorDark =
      Color(0xFFEF5350); // Lighter error for dark theme
  static const Color successDark =
      Color(0xFF66BB6A); // Lighter success for dark theme
  static const Color warningDark =
      Color(0xFFFF9800); // Lighter warning for dark theme
  static const Color textPrimaryDark =
      Color(0xFFFFFFFF); // White text for dark theme
  static const Color textSecondaryDark =
      Color(0xFFBDBDBD); // Light gray for dark theme
  static const Color dividerDark = Color(0xFF424242); // Dark divider

  // Additional colors for Persian food service context
  static const Color cardLight = Color(0xFFF8F9FA);
  static const Color cardDark = Color(0xFF2D2D2D);
  static const Color shadowLight = Color(0x0F000000);
  static const Color shadowDark = Color(0x1FFFFFFF);

  /// Light theme
  static ThemeData lightTheme = ThemeData(
    brightness: Brightness.light,
    colorScheme: ColorScheme(
      brightness: Brightness.light,
      primary: primaryLight,
      onPrimary: Colors.white,
      primaryContainer: primaryLight.withAlpha(26),
      onPrimaryContainer: primaryLight,
      secondary: secondaryLight,
      onSecondary: Colors.white,
      secondaryContainer: secondaryLight.withAlpha(26),
      onSecondaryContainer: secondaryLight,
      tertiary: warningLight,
      onTertiary: Colors.white,
      tertiaryContainer: warningLight.withAlpha(26),
      onTertiaryContainer: warningLight,
      error: errorLight,
      onError: Colors.white,
      errorContainer: errorLight.withAlpha(26),
      onErrorContainer: errorLight,
      surface: surfaceLight,
      onSurface: textPrimaryLight,
      onSurfaceVariant: textSecondaryLight,
      outline: dividerLight,
      outlineVariant: dividerLight.withAlpha(128),
      shadow: shadowLight,
      scrim: Colors.black54,
      inverseSurface: surfaceDark,
      onInverseSurface: textPrimaryDark,
      inversePrimary: primaryDark,
    ),
    scaffoldBackgroundColor: backgroundLight,
    cardColor: cardLight,
    dividerColor: dividerLight,

    // AppBar theme with minimal elevation
    appBarTheme: AppBarTheme(
      backgroundColor: backgroundLight,
      foregroundColor: textPrimaryLight,
      elevation: 1.0,
      shadowColor: shadowLight,
      titleTextStyle: GoogleFonts.inter(
        fontSize: 20,
        fontWeight: FontWeight.w600,
        color: textPrimaryLight,
      ),
      iconTheme: IconThemeData(color: textPrimaryLight),
    ),

    // Card theme with subtle elevation
    cardTheme: CardTheme(
      color: cardLight,
      elevation: 2.0,
      shadowColor: shadowLight,
      shape: RoundedRectangleBorder(
        borderRadius: BorderRadius.circular(12.0),
      ),
      margin: const EdgeInsets.symmetric(horizontal: 16, vertical: 8),
    ),

    // Bottom navigation optimized for Persian users
    bottomNavigationBarTheme: BottomNavigationBarThemeData(
      backgroundColor: surfaceLight,
      selectedItemColor: primaryLight,
      unselectedItemColor: textSecondaryLight,
      elevation: 4.0,
      type: BottomNavigationBarType.fixed,
    ),

    // Floating action button with contextual appearance
    floatingActionButtonTheme: FloatingActionButtonThemeData(
      backgroundColor: primaryLight,
      foregroundColor: Colors.white,
      elevation: 4.0,
      shape: RoundedRectangleBorder(
        borderRadius: BorderRadius.circular(16.0),
      ),
    ),

    // Button themes optimized for mobile authentication
    elevatedButtonTheme: ElevatedButtonThemeData(
      style: ElevatedButton.styleFrom(
        foregroundColor: Colors.white,
        backgroundColor: primaryLight,
        padding: const EdgeInsets.symmetric(horizontal: 24, vertical: 16),
